    private void btnIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIgualActionPerformed
        // TODO add your handling code here:
        segundoNumero = Double.parseDouble(txtResultado.getText());
        if(sinal.equals("soma") ) {
            txtResultado.setText(String.valueOf(numero.Somar(primeiroNumero, segundoNumero)));
        }
        
        if(sinal.equals("subtrair") ) {
            txtResultado.setText(String.valueOf(numero.Subtrair(primeiroNumero, segundoNumero)));
        }
        
        if(sinal.equals("dividir") ) {
            txtResultado.setText(String.valueOf(numero.Dividir(primeiroNumero, segundoNumero)));
        }
        
        if(sinal.equals("multiplicar") ) {
            txtResultado.setText(String.valueOf(numero.Multiplicar(primeiroNumero, segundoNumero)));
        }
    }//GEN-LAST:event_btnIgualActionPerformed

